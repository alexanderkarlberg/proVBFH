      COMPLEX*16 W(6,NWAVEFUNCS),WX1(6),WX2(6)
      INTEGER NHEL(3:4), ihel
      DATA (NHEL(IHEL),IHEL=3,4) / +1,+1/ ! do not care
      Save NHEL
c  lepton spinors and W+- polarization vectors
