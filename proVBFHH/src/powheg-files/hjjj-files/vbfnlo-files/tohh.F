         do i=1,6
            w(i,3) = wp(i)
            w(i,4) = wm(i)
         enddo
      endif
      DO MU = 0,3
         DO NU = 0,3
            CALL VCARTX(Q1(0),WMASS ,WWIDTH, MU,-1,W(1,1   ))        
            CALL VCARTX(Q2(0),WMASS ,WWIDTH, NU,-1,W(1,2   ))
             if (.not.lswitch) then
               CALL SXXXXX(P(0,3   ),1,W(1,3   ))
               CALL SXXXXX(P(0,4   ),1,W(1,4   ))
            endif
            CALL JVSXXX(W(1,2),W(1,3),GWWH ,WMASS,WWIDTH,W(1, 5   ))           
